    private func fetchData(from url: String?) {
        NetworkManager.shared.fetchData(from: url) { marsRoverPhotos in
            self.marsRoverPhoto = marsRoverPhotos
            self.mars = marsRoverPhotos.photos
            self.tableView.reloadData()
        }
    }
    
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        guard let indexPath = tableView.indexPathForSelectedRow else { return }
        let photo = marsRoverPhoto?.photos[indexPath.row]
        let detailVC = segue.destination as! DetailPhotoViewController
        detailVC.marsPhoto = photo
        
    }

