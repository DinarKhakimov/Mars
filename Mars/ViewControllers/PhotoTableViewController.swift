    private var marsRoverPhoto: MarsRoverPhotos?
    var mars: [Photo] = []
    

    override func viewDidLoad() {
        super.viewDidLoad()
        tableView.rowHeight = 100
        fetchData(from: Link.marsRoverPhotApi.rawValue)
